<H1>Training</H1>
<p>The Club generally trains 3 or 4 times a week, although there is some variation depending on approaching competitions, vacation periods, and individual commitments. Normal club training times are weekdays between 5 and 8pm, and Saturday 12-2pm. Most members train for about an hour between the times indicated - it is rare, and rarely productive, to train for 2 hours.
</p>
<p>Experienced lifters determine their own training programs. Less experienced lifters are generally advised to follow a beginner or intermediate program.
</p>
<p>Training is generally periodised - meaning that the kind of training we do goes in cycles. For instance, you might train 6 weeks for muscular endurance, 8 weeks for hypertrophy, then 8 weeks for strength in the run-up to a competition.
</p>
