<H2>How places are decided</H3>
The largest single lift that a lifter has achieved in each of the individual lifts is added together to 
give a total of the three lifts. Simply, the lifter with the largest total wins!</P>

<H2>Questions & Answers</H2>
<H4>Do powerlifters only do these three exercises?</H4>
<p>The squat, bench press and deadlift are the competition lifts. In training, powerlifters regularly employ many other exercises for the sake of muscular balance, to avoid injury and to assist with the competition lifts.
</p>
<H4>Isn't it an unfair advantage if one lifter is bigger than another?</H4>
<p>
Powerlifting is competed in a number of bodyweight classes, so lifters only compete against others of 
similar weight. In some competitions, including team events (such as the Varsity Match), lifters 
compete directly against each other at all weights. In this instance a mathematical formula (the Wilkes Formula)
is used to adjust each lifter's total according to bodyweight so that no-one has an unfair advantage.
</p>
<H4>I couldn't be a powerlifter, I'm not huge!?</H4>
<p>
In fact, many powerlifters are not the huge, obviously muscular people everyone assumes. The sport can be 
particularly competitive at the lower bodyweight classes, and in fact the Oxford 1st team in the last Varsity 
match were almost all below 75kg bodyweight!
</p>
<p>
Don't let a perceived lack of size or strength put you off, have a go at powerlifting and you never know, 
you might just enjoy it.
</p>
<H4>Isn't it true that all powerlifters do drugs?</H4>
<p>
Also not true. Powerlifting, along with Weightlifting and Athletics, is incredibly heavily subject to drug testing
at all age and ability levels in the UK. This means that Powerlifting and weightlifting are two of the cleanest 
sports around in the UK now.
</p>
