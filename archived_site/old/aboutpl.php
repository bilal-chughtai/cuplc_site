<H1>About Powerlifting</H1>
<p>Powerlifting is just one of a large range of strength sports. 
The competition consists of three lifts, the Back Squat, Bench Press and the Dead Lift.
Each competitior has three attempts at each type of lift 
(he/she performs three squat attempts, three bench presses, and three dead lifts). 
<B> NOTE</B> Three attempts at each lift in total, not at each weight!</p>

<p>
